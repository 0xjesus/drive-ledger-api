  /**
   * Iniciar simulaci√≥n de conducci√≥n
   * POST /api/simulations
   */
  static async startSimulation(req, res) {
    try {
      console.log('üìã startSimulation - Request body:', req.body);
      const { routeType, durationMinutes, walletAddress } = req.body;

      console.log(`üìù Parameters: routeType=${routeType}, durationMinutes=${durationMinutes}, walletAddress=${walletAddress}`);

      if (!routeType || !durationMinutes) {
        console.log('‚õî Missing required parameters');
        return res.respond({
          success: false,
          status: 400,
          message: 'Route type and duration are required'
        });
      }

      // Buscar o crear usuario si se proporciona direcci√≥n de wallet
      let userId = null;
      if (walletAddress) {
        console.log(`üîç Looking up user for wallet: ${walletAddress}`);
        const user = await prisma.user.upsert({
          where: { walletAddress },
          update: {},
          create: { walletAddress }
        });
        userId = user.id;
        console.log(`üë§ User resolved with ID: ${userId}`);
      }

      // Verificar si ya hay una simulaci√≥n en ejecuci√≥n
      console.log(`üîÑ Checking simulation status. Current isStreaming value: ${DriveDataSimulator.isStreaming}`);
      if (DriveDataSimulator.isStreaming) {
        console.log('‚ö†Ô∏è A simulation is already running');
        return res.respond({
          success: false,
          status: 400,
          message: 'A simulation is already running'
        });
      }

      // Verificar si hay datos cargados
      console.log(`üìä Checking synthetic data. Data length: ${DriveDataSimulator.syntheticData?.length || 'undefined'}`);
      if (!DriveDataSimulator.syntheticData || DriveDataSimulator.syntheticData.length === 0) {
        console.log('‚ö†Ô∏è No synthetic data available');
        try {
          console.log('üîÑ Attempting to load synthetic data');
          await DriveDataSimulator.loadSyntheticData();
          console.log(`‚úÖ Synthetic data loaded. Count: ${DriveDataSimulator.syntheticData.length}`);
        } catch (loadError) {
          console.error('‚ùå Failed to load synthetic data:', loadError);
          console.error('‚ùå Stack trace:', loadError.stack);
          return res.respond({
            success: false,
            status: 500,
            message: `Failed to load simulation data: ${loadError.message}`
          });
        }
      }

      // Iniciar simulaci√≥n
      console.log(`üöÄ Starting simulation with routeType=${routeType}, durationMinutes=${durationMinutes}`);
      let dataStream;
      try {
        // Guardamos el valor actual para comparar
        const wasStreaming = DriveDataSimulator.isStreaming;
        console.log(`üëâ Before startSimulation call: isStreaming=${wasStreaming}`);

        // Verifiquemos que DriveDataSimulator y su m√©todo startSimulation existen
        console.log(`üîç DriveDataSimulator exists: ${!!DriveDataSimulator}`);
        console.log(`üîç startSimulation method exists: ${!!DriveDataSimulator.startSimulation}`);
        console.log(`üîç Type of startSimulation: ${typeof DriveDataSimulator.startSimulation}`);

        // Llamamos al m√©todo con los par√°metros
        dataStream = DriveDataSimulator.startSimulation(
          routeType,
          Number(durationMinutes)
        );

        // Verifiquemos el resultado
        console.log(`üëâ After startSimulation call: dataStream=${!!dataStream}, type=${typeof dataStream}`);
        console.log(`üëâ After startSimulation call: isStreaming=${DriveDataSimulator.isStreaming}`);

        if (!dataStream) {
          console.log('‚ö†Ô∏è No data stream returned from startSimulation');
          return res.respond({
            success: false,
            status: 400,
            message: 'Failed to start simulation: No data stream returned'
          });
        }

        // Si isStreaming no cambi√≥, hay un problema en el m√©todo startSimulation
        if (wasStreaming === DriveDataSimulator.isStreaming && !DriveDataSimulator.isStreaming) {
          console.log('‚ö†Ô∏è startSimulation did not set isStreaming flag to true');
        }

      } catch (simError) {
        console.error('‚ùå Error in startSimulation call:', simError);
        console.error('‚ùå Stack trace:', simError.stack);
        return res.respond({
          success: false,
          status: 400,
          message: `Failed to start simulation: ${simError.message}`
        });
      }

      // Buscar la ruta para incluir detalles en la respuesta
      console.log(`üîç Looking up route details for: ${routeType}`);
      const simulationRoute = await prisma.simulationRoute.findUnique({
        where: { routeType }
      });

      // Log del resultado de la b√∫squeda de ruta
      console.log(`üëâ Route lookup result: ${!!simulationRoute ? 'Found' : 'Not found'}`);
      if (simulationRoute) {
        console.log(`üëâ Route details: ${JSON.stringify(simulationRoute)}`);
      }

      // Si no se encuentra la ruta, mostrar un error m√°s descriptivo
      if (!simulationRoute) {
        console.log(`‚ö†Ô∏è Route type "${routeType}" not found, stopping simulation`);
        try {
          DriveDataSimulator.stopSimulation(); // Detener la simulaci√≥n iniciada
          console.log('‚úÖ Simulation stopped successfully');
        } catch (stopError) {
          console.error('‚ùå Error stopping simulation:', stopError);
          console.error('‚ùå Stack trace:', stopError.stack);
        }

        return res.respond({
          success: false,
          status: 400,
          message: `Route type "${routeType}" not found`
        });
      }

      // Registrar simulaci√≥n en la base de datos
      console.log('üíæ Creating simulation record in database');
      const simulation = await prisma.simulation.create({
        data: {
          routeType,
          userId: userId,
          status: 'RUNNING',
          dataPointsCount: 0
        }
      });
      console.log(`‚úÖ Simulation record created with ID: ${simulation.id}`);

      // Enriquecer la respuesta con datos adicionales
      console.log('üèÅ Simulation started successfully, building response');
      const response = {
        success: true,
        data: {
          simulationId: simulation.id,
          route: simulationRoute.name,
          routeType: routeType,
          duration: Number(durationMinutes),
          routeDetails: simulationRoute,
          userId: userId,
          walletAddress: walletAddress || 'anonymous',
          startedAt: simulation.createdAt.toISOString(),
          estimatedCompletionTime: new Date(Date.now() + (durationMinutes * 60 * 1000)).toISOString()
        },
        message: 'Simulation started successfully'
      };
      console.log('üì§ Respuesta:', response);
      return res.respond(response);
    } catch (error) {
      console.error('‚ùå CRITICAL ERROR in startSimulation:', error);
      console.error('‚ùå Stack trace:', error.stack);
      return res.respond({
        success: false,
        status: 500,
        message: `Failed to start simulation: ${error.message}`
      });
    }
  }

