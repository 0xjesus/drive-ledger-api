  /**
   * Detener simulaci√≥n actual
   * POST /api/simulations/stop
   */
  static async stopSimulation(req, res) {
    console.log('üìã stopSimulation - Request body:', req.body);
    try {
      console.log('üõë Stopping simulation');
      const result = DriveDataSimulator.stopSimulation();
      console.log('üëâ Stop result:', result);

      if (!result.success) {
        console.log('‚ö†Ô∏è Failed to stop simulation:', result.message);
        return res.respond({
          success: false,
          status: 400,
          message: result.message
        });
      }

      // Actualizar registro de simulaci√≥n en la base de datos
      console.log('üîç Looking for active simulation in database');
      const simulation = await prisma.simulation.findFirst({
        where: { status: 'RUNNING' },
        orderBy: { createdAt: 'desc' }
      });
      console.log('üëâ Found simulation:', simulation);

      if (simulation) {
        console.log(`üíæ Updating simulation ${simulation.id} in database`);
        await prisma.simulation.update({
          where: { id: simulation.id },
          data: {
            status: 'COMPLETED',
            endedAt: new Date(),
            durationMinutes: result.summary.durationMinutes,
            distanceKm: result.summary.distanceKm,
            avgSpeedKmph: result.summary.averageSpeedKmph,
            maxSpeedKmph: result.summary.maxSpeedKmph,
            efficiencyScore: result.summary.efficiencyScore,
            dataPointsCount: result.summary.dataPointsCollected,
            rawData: DriveDataSimulator.simulationData.slice(0, 20), // Guardar solo algunos puntos de muestra
            diagnosticIssues: result.summary.diagnosticIssues
          }
        });
        console.log('‚úÖ Simulation updated successfully');
      }

      const response = {
        success: true,
        data: {
          ...result,
          simulationId: simulation?.id
        },
        message: 'Simulation stopped successfully'
      };
      console.log('üì§ Respuesta:', response);
      return res.respond(response);
    } catch (error) {
      console.error('‚ùå Error stopping simulation:', error);
      console.error('‚ùå Stack trace:', error.stack);
      return res.respond({
        success: false,
        status: 500,
        message: `Failed to stop simulation: ${error.message}`
      });
    }
  }

  /**
   * Generar recompensa por datos recolectados
   * POST /api/rewards
   */
  static async generateReward(req, res) {
    console.log('üìã generateReward - Request body:', req.body);
    try {
      const { walletAddress, simulationId } = req.body;
      console.log(`üìù Parameters: walletAddress=${walletAddress}, simulationId=${simulationId}`);

      if (!walletAddress) {
        console.log('‚õî Missing required parameter: walletAddress');
        return res.respond({
          success: false,
          status: 400,
          message: 'Wallet address is required'
        });
      }

      // Verificar si la simulaci√≥n existe
      let simulation = null;
      if (simulationId) {
        console.log(`üîç Looking up simulation with ID: ${simulationId}`);
        simulation = await prisma.simulation.findUnique({
          where: { id: simulationId }
        });
        console.log('üëâ Simulation found:', simulation);

        if (!simulation) {
          console.log(`‚ö†Ô∏è Simulation with ID ${simulationId} not found`);
          return res.respond({
            success: false,
            status: 404,
            message: `Simulation with ID ${simulationId} not found`
          });
        }
      }

      // Obtener datos de simulaci√≥n
      console.log('üîç Checking simulation data');
      const simulationData = DriveDataSimulator.simulationData;
      console.log(`üëâ Simulation data length: ${simulationData?.length || 0}`);

      if (!simulationData || simulationData.length === 0) {
        console.log('‚ö†Ô∏è No simulation data available for reward');
        return res.respond({
          success: false,
          status: 400,
          message: 'No simulation data available for reward'
        });
      }

      // Calcular recompensa
      console.log('üí∞ Calculating reward amount');
      const rewardAmount = DriveDataSimulator.getDataBatchRewardValue(simulationData);
      console.log(`üëâ Calculated reward amount: ${rewardAmount}`);

      // Generar transacci√≥n de recompensa
      console.log(`üí∞ Processing reward of ${rewardAmount} tokens for ${walletAddress}`);
      const rewardResult = await TokenRewardManager.mintRewardTokens(
        walletAddress,
        rewardAmount,
        simulationId
      );
      console.log('üëâ Reward result:', rewardResult);

      const response = {
        success: rewardResult.success,
        data: rewardResult,
        message: rewardResult.success
          ? `Reward of ${rewardAmount} DRVL tokens processed for ${walletAddress}`
          : `Reward requires frontend signing: ${rewardAmount} DRVL tokens for ${walletAddress}`
      };
      console.log('üì§ Respuesta:', response);
      return res.respond(response);
    } catch (error) {
      console.error('‚ùå Error generating reward:', error);
      console.error('‚ùå Stack trace:', error.stack);
      return res.respond({
        success: false,
        status: 500,
        message: `Failed to generate reward: ${error.message}`
      });
    }
  }

